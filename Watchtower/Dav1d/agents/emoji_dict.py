def get_emoji(keyword: str) -> str:
    """
    Get an emoji by keyword.
    
    Args:
        keyword: The emoji keyword (e.g., 'rocket', 'fire', 'brain')
    
    Returns:
        The emoji character, or empty string if not found
    """
    return EMOJI_DICT.get(keyword.lower(), '')


def emojify_text(text: str, density: str = 'medium') -> str:
    """
    Add emojis to text based on word matching.
    
    Args:
        text: The text to emojify
        density: 'low', 'medium', or 'high' - controls emoji frequency
    
    Returns:
        Text with emojis inserted
    """
    words = text.split()
    result = []
    
    # Set emoji insertion rate based on density
    rates = {'low': 0.2, 'medium': 0.4, 'high': 0.6}
    insert_rate = rates.get(density, 0.4)
    
    import random
    for word in words:
        result.append(word)
        # Clean word for matching (remove punctuation)
        clean_word = word.lower().strip('.,!?;:')
        
        # Check if word has an emoji mapping
        if clean_word in WORD_TO_EMOJI and random.random() < insert_rate:
            result.append(WORD_TO_EMOJI[clean_word])
    
    return ' '.join(result)


